#include "libft.h"

void	ft_strclr(char *s)
{
	int		i;
	int		j;

	if (s)
	{
		i = 0;
		j = ft_strlen(s);
		while (i <= j)
		{
			s[i] = '\0';
			i++;
		}
	}
}
