#include "libft.h"

void	ft_putstr_w(const wchar_t *s)
{
	int	i;

	if (s == NULL)
	{
		write(1, "(null)", 6);
		return ;
	}
	i = -1;
	while (s[++i])
		write(1, &s[i], 1);
	return ;
}
