#include "libft.h"

int	ft_atoi(const char *str)
{
	char	*s;
	long	i;
	long	neg;
	long	tot;

	neg = 1;
	i = 0;
	s = (char *)str;
	while (s[i] == ' ' || (s[i] > 8 && s[i] < 14))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			neg = (-1);
		i++;
	}
	tot = 0;
	while (s[i] >= '0' && s[i] <= '9')
	{
		tot = tot * 10 + (s[i] - '0');
		i++;
	}
	return ((int)(tot * neg));
}
